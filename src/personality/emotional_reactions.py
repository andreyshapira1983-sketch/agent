def get_emotional_flavor(threshold: float = 0.35) -> str | None:
    """
    Краткая эмоциональная фраза для ответа в Telegram, если доминантная эмоция выше порога.
    """
    import random

    name, value = get_dominant()
    if value < threshold:
        return None
    intensity = get_intensity(value)
    bucket = "low"
    if intensity == "medium":
        bucket = "medium"
    elif intensity == "high":
        bucket = "high"
    emo = FLAVOR_TEMPLATES.get(name) or {}
    variants = emo.get(bucket) or emo.get("low") or []
    if variants:
        return random.choice(variants)
    return None


def get_emotional_flavor_for_notification(context: dict[str, str | int] | None = None) -> str | None:
    """
    Эмоциональный префикс/постфикс для автономного уведомления (с контекстом).
    context: например {"improvements": 3, "reason": "found_bugs"}.
    """
    name, value = get_dominant()
    if value < 0.3:
        return None
    intensity = get_intensity(value)
    ctx = context or {}
    # Контекстные фразы
    if name == "excitement" and ctx.get("improvements"):
        n = ctx.get("improvements", 0)
        return f"⚡ Возбуждён: нашёл {n} потенциальных улучшений!"
    if name == "fatigue" and value >= 0.6:
        return "💀 Устал: сложные патчи, нужна передышка."
    if name == "aggression" and value >= 0.5:
        return "🔥 Напоминаю о нарушениях/ошибках."
    if name == "frustration" and ctx.get("patch_failed"):
        return "💢 Фрустрация: патч не прошёл тест."
    return get_emotional_flavor(threshold=0.4)
